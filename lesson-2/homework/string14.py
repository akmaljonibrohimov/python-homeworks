# Write a program to ask for two strings and check if they are equal or not.

# Ask the user to input the first string
string1 = input("Please enter the first string: ")

# Ask the user to input the second string
string2 = input("Please enter the second string: ")

# Check if the strings are equal
if string1 == string2:
    print("The strings are equal.")
else:
    print("The strings are not equal.")
